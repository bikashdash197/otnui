					<div class="otv-faqs-text">
						<p>Ans: - Though not much has been talked about the Odia cuisines but Odisha is one such destination, where food connoisseur would be amazed to relish some of the best Odia food prepared in every household. Odia cuisines are unique and healthy as they are prepared with less oil and spices.&nbsp;</p>
						<p>Here are some popular cuisines of Odisha-</p>
						<p>
							<br> &nbsp; &nbsp; • Pakhala Bhata- It is a popular dish during summer in every household of Odisha. It is prepared with cooked rice soaked in water overnight. There are many variations of pakhala often served with fried fish, badi choora, saga bhaja, aloo bharta among others.</p>
						<p>&nbsp; &nbsp; • Dalma- It is a regular dish in Odisha. Instead of plain Dal, Odia people prefer Dalma. It is made with pulses (Mostly Moong Dal), vegetables like pumpkin, potato, papaya, yam, brinjal and other seasonal vegetables, with spices and a tadka.&nbsp;
							<br> &nbsp; &nbsp; • Khechidi:-It is a rice dish cooked with lentils and given a flavor with a tadka of cumin and red chilly and Deshi ghee.</p>
						<p>&nbsp; &nbsp; • Santula- It is often made with various vegetables available during various seasons. While potato, brinjal are most commonly used to prepare this dish, drumstick, papaya,&nbsp;beans are also used. Boiled veggies are given a flavor with a tadaka of mustard seed, onion and chilly crackled in oil. Santula is a healthy dish, with no spices and less oil.</p>
						<p>&nbsp; &nbsp; • Chaatu Rai: - A dish made from mushrooms and mustard paste.</p>
						<p>&nbsp; &nbsp; • Potala alu rasa- It is another famous dish made with potatoes, pointed gourd. The dish is unique with distinctive flavors of various spices and thick gravy.
							<br> &nbsp; &nbsp; • Machha Besara: - A fish curry prepared with mustard paste.
							<br> &nbsp; &nbsp; • Tomato Khata:-A sweet-and –sour dish made from tomato and jaggery.</p>
						<p>&nbsp; &nbsp; • Chhena Poda - One of the favorite sweets of the state, Chena Poda is prepared with cottage cheese, semolina and sugar syrup. The dessert is baked for hours which give a caramelized and crispy look
