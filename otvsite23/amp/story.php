               <div class="mobile-story-content">
                  <p>Amajor train mishap was averted on Tuesday when a Memu train managed a narrow escape near Baruna Singh chhak at Nilgiri Road railway station in Odisha’s Balasore district.</p>
                  <p>As per initial reports, the train entered a loop line track where some work was going on. A tragedy was reportedly averted only due to the presence of the loco pilot who sensed something wrong and applied brakes.</p>
                  <p>The Memu train was on its way to Balasore from Bhadrak district when the loco pilot applied brakes after noticing some mistake in the track endpoint. </p>
                  <p>Reportedly, the loco pilot then reversed the train towards Balasore, affecting the movement of other trains on the route for some time.</p>
                  <p>Bijaydhar Barik, a passenger said, “It was only the train driver who spotted something wrong in the tracks. The train later reversed some 200 to 300 meters and we got down from the train. The train was moving at slow speed and stopped at a distance from the track which had some issues.”</p>
                  <p>Though a tragedy was averted, it has created fears in the minds of passengers travelling in the train. </p>
                  <p>In June, a major train tragedy in Balasore sent shockwaves across the globe. Over 290 passengers died in the train accident involving Coromandel Express and a goods train.</p>
                  <p>Speaking on the incident, the loco pilot of the Memu train said, “The train was moving at a speed of 7 to 8 kmph and the track was set in the opposite direction as some construction work was going on… The point could have busted… There was no chance of any derailment of the train but tracks would have damaged.”</p>
